package beans;

public class UtilisateurBean {
	private String nom;
	private String prenom;
	private Integer annNaissance;

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}

	public Integer getAnnNaissance() {
		return annNaissance;
	}

	public void setAnnNaissance(Integer annNaissance) {
		this.annNaissance = annNaissance;
	}
}
